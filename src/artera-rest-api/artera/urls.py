from django.contrib import admin
from rest_framework.routers import DefaultRouter
from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
)
from django.urls import path, include

from artworks.views import ArtworksViewSet
from collectionss.views import CollectionViewSet
from users.views import (
    ProfileViewSet,
    AuthViewSet
)

router = DefaultRouter()
router.register(r'artworks', ArtworksViewSet, basename='artworks')
router.register(r'collections', CollectionViewSet, basename='collections')
router.register(r'profiles', ProfileViewSet, basename='profiles')
router.register(r'accounts', AuthViewSet, basename='accounts')

urlpatterns = [
    path('admin/', admin.site.urls),
    path('api/', include(router.urls)),
    path('api/token/', TokenObtainPairView.as_view(), name='token_obtain_pair'),
    path('api/token/refresh/', TokenRefreshView.as_view(), name='token_refresh'),
]
